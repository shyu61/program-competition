int main() {
    int n, m; cin >> n >> m;
    vector<vector<int>> g(n);
    rep(i, m) {
        int u, v; cin >> u >> v, u--, v--;
        g[u].push_back(v);
    }
    int s, t; cin >> s >> t, s--, t--;

    queue<P> que; que.emplace(s, 0);
    vector<vector<int>> dist(n, vector<int>(3, -1));
    dist[s][0] = 0;
    while (!que.empty()) {
        auto [v, d] = que.front(); que.pop();
        if (v == t && d % 3 == 0) {
            cout << d / 3 << endl;
            return 0;
        }
        for (auto u : g[v]) {
            int step = (d + 1) % 3;
            if (dist[u][step] != -1) continue;
            que.emplace(u, d + 1);
            dist[u][step] = d + 1;
        }
    }
    cout << -1 << endl;
}
